    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        btnNuevo.setEnabled(false);
        txtCedula.setEnabled(true);
        if (txtCedula.getText().length()!= 0) {
            if (txtCedula.getText().matches("[0-9]*") == false || txtCedula.getText().length() > 10 || txtCedula.getText().length() < 10) {
                btnModificar.setEnabled(false);
                btnEliminar.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Cedula Incorrecta");
            } else {
                btnModificar.setEnabled(true);
                btnEliminar.setEnabled(true);
                updateTabla();
                txtCedula.setText(String.valueOf(Tabla.getValueAt(fila, 0)));
                txtNombre.setText(String.valueOf(Tabla.getValueAt(fila, 1)));
                txtApellido.setText(String.valueOf(Tabla.getValueAt(fila, 2)));
                txtTelefono.setText(String.valueOf(Tabla.getValueAt(fila, 3)));
                cboEspecialidad.setSelectedItem(Tabla.getValueAt(fila, 4).toString());
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

