    private void Limpiar() {
        txtNombre.setText("");
        txtCedula.setText("");
        txtTelefono.setText("");
        txtApellido.setText("");
        cboEspecialidad.setSelectedItem("---Seleccione---");
        txtUsuario.setText("");
        txtContra.setText("");
        txtContra1.setText("");
    }

    private void updateTabla() {
        String[] columNames = {"Cedula", "Nombre", "Apellido", "Telefono", "Especialidad"};
        Medico d = new Medico();
        dtPer = d.getDatos();
        DefaultTableModel datos = new DefaultTableModel(dtPer, columNames);
        Tabla.setModel(datos);
        TableColumn columna = Tabla.getColumn("Cedula");
        columna.setPreferredWidth(50);
        columna.setMinWidth(10);
        columna.setMaxWidth(30);
    }

