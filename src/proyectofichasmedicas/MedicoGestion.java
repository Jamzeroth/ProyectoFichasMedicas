    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        // TODO add your handling code here:
        Medico d = new Medico();
        String tipo = cboEspecialidad.getSelectedItem().toString();
        String cedula = txtCedula.getText().toString();
        int num = cedula.length();
        if (tipo == "--Seleccione--" || cedula.matches("[0-9]*") == false || num > 10 || num < 10) {
            if (tipo == "--Seleccione--") {
                JOptionPane.showMessageDialog(null, "Especialidad No Elejida");
            }
            if (cedula.matches("[0-9]*") == false || num > 10 || num < 10) {
                JOptionPane.showMessageDialog(null, "Cedula Incorrecta");
            }
            tipo = cboEspecialidad.getSelectedItem().toString();
        } else {

            d.InsertarDoctor(
                    cedula,
                    txtNombre.getText().toString(),
                    txtApellido.getText().toString(),
                    txtTelefono.getText().toString(),
                    tipo,
                    txtUsuario.getText().toString(),
                    txtContra.getText().toString());

            Limpiar();
            btnEliminar.setEnabled(false);
            btnGrabar.setEnabled(false);
            btnNuevo.setEnabled(true);
            btnModificar.setEnabled(false);
            btnConsultar.setEnabled(true);

            txtCedula.setEnabled(false);
            txtTelefono.setEnabled(false);
            txtNombre.setEnabled(false);
            txtApellido.setEnabled(false);
            cboEspecialidad.setEnabled(false);
            txtUsuario.setEnabled(false);
            txtContra.setEnabled(false);
            txtContra1.setEnabled(false);
        }
        updateTabla();

    }//GEN-LAST:event_btnGrabarActionPerformed

